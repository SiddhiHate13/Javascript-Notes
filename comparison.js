// Comparison Operators 
const x = 5, y = 2, z = 'hello';

// equal to operator
console.log(x == 5);     
console.log(y == '2');   
console.log(z == 'Hello');  

// not equal operator
console.log(x != 2); 
console.log(y != 'Hello'); 

// strict equal operator
console.log(x === 2);
console.log(x === '2'); 

// strict not equal operator
console.log(x !== 2); 
console.log(x !== '2'); 
console.log(y !== 'Hello'); 

// greater than operator
console.log(y > 2); 

// greater than or equal operator
console.log(y >= 3);

// less than operator
console.log(y < 2); 
console.log(x < 3);

// less than or equal operator
console.log(x <= 3) 
console.log(y <= 2);